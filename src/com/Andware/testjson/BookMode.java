import android.R.integer;

public class BookMode {

	private int id;
	private String name;
	private String discribeBody;
	private int firstChapterId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDiscribeBody() {
		return discribeBody;
	}
	public void setDiscribeBody(String body) {
		this.discribeBody = body;
	}
	public int getFirstChapterId() {
		return firstChapterId;
	}
	public void setFirstChapterId(int firstChapterId) {
		this.firstChapterId = firstChapterId;
	}
	
	
}
